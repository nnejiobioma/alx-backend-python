#!/usr/bin/env python3
"""
This is a basic annotation
adds two float
"""

def add(a: float, b: float) -> float:
    """
    Add two floats and return the result as a float.

    Args:
        a (float): The first float.
        b (float): The second float.

    Returns:
        float: The sum of 'a' and 'b'.
    """
    return a + b
